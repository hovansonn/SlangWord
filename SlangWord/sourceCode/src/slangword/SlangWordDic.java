    private void jButton12ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton12ActionPerformed
        SlangWord word = new SlangWord();
        word.setSlang(jTextField5.getText());
        word.setDefinition(jTextField6.getText());
        for (SlangWord slangWord :
                slangWords) {
            if (slangWord.getSlang().compareTo(word.getSlang()) == 0) {
                int YesOrNo = JOptionPane.showConfirmDialog(null,"Do you want to overwrite?(yes - Overwrite| no - duplicate ","Overwrite or Duplicate",JOptionPane.YES_NO_OPTION);
                if(YesOrNo == 0)
                    slangWord.setDefinition(word.getDefinition());
                else
                    
                    slangWord.setDefinition(slangWord.getDefinition()+" | "+word.getDefinition());
                return;
            }
        }
        slangWords.add(word);
        JOptionPane.showMessageDialog(null,"Add Successfully");
        
    }//GEN-LAST:event_jButton12ActionPerformed
