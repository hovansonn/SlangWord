    private static void loadSlangWord() {
        BufferedReader reader;
        try {
            reader = new BufferedReader(new FileReader("slang.txt"));
            String line = reader.readLine();
            while (line != null) {
                String define = "";
                String slang = line.split("`")[0];
                if (line.contains("`"))
                    define = line.split("`")[1];
                else
                    System.out.println(line);
                SlangWord word = new SlangWord(slang, define);
                slangWords.add(word);
                line = reader.readLine();
            }
            slangs = new ArrayList<SlangWord>(slangWords);
            reader.close();
        } catch (IOException e) {
            System.out.println(e.getMessage());
        }
        
    }

    private static String feature1(String slang) {
        for (SlangWord word :
                slangWords) {
            if (word.getSlang().compareTo(slang) == 0) {
                historySearch.add(word);
                return word.getDefinition();
            }
        }
        return slang + " NOT FOUND DEFINITION";
    }

    private static String feature2(String definition) {
        StringJoiner result = new StringJoiner(" | ");
        for (SlangWord word :
                slangWords) {
            if (word.getDefinition().compareTo(definition) == 0) {
                result.add(word.getSlang());
                historySearch.add(word);
                continue;
            }
            if(word.getDefinition().contains("|"))
            {
                String[] listDefine = word.getDefinition().split("|");
                for (int i = 0; i < listDefine.length; i++) {
                    if (definition.compareTo(listDefine[i]) == 0) {
                        result.add(word.getSlang());
                        historySearch.add(word);
                        continue;
                    }
                }
            }
            
        }
        return result.toString();
    }


